        System.out.println("Default value of int     : " + p2.i);
        System.out.println("Default value of long    : " + p2.l);
        System.out.println("Default value of float   : " + p2.f);
        System.out.println("Default value of double  : " + p2.d);
        System.out.println("Default value of char    : [" + p2.c + "]");
